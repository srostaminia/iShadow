function format_graph_paperQ(hTitle,hXLabel, hYLabel,hLegend,plotFileName)



set( gca                       , ...
    'FontName'   , 'Calibri' );
set([hTitle, hXLabel, hYLabel], ...
    'FontName'   , 'Calibri');
set([hLegend, gca]             , ...
    'FontSize'   , 15           );
set([hXLabel, hYLabel]  , ...
    'FontSize'   , 15          );
set( hTitle                    , ...
    'FontSize'   , 15          , ...
    'FontWeight' , 'bold'      );

set(gca, ...
  'Box'         , 'off'     , ...
  'TickDir'     , 'out'     , ...
  'TickLength'  , [.01 .01] , ...
  'XMinorTick'  , 'off'      , ...
  'YMinorTick'  , 'off'      , ...
  'YGrid'       , 'off'      , ...
  'XColor'      , [.3 .3 .3], ...
  'YColor'      , [.3 .3 .3], ...
  'LineWidth'   , 1         );

%  'YTick'       , 0:500:2500, ...

set(gcf, 'PaperPositionMode', 'auto');
print('-dpng','-r0',plotFileName)

%close;


