function plotFormatQ_final(pathToSave,plotFileName,saveOn)

set(gca,'LineWidth',1);
set(gcf, 'PaperPosition', [0  0 5 3.5]);  
set(gcf, 'PaperSize', [5 3.5]); 

if saveOn
    origin=pwd;
    
    cd ~
    cd(pathToSave);
    
    saveas(gcf, plotFileName,'png')
    cd (origin);

end


set(gca, ...
  'Box'         , 'off'     , ...
  'TickDir'     , 'out'     , ...
  'TickLength'  , [.01 .01] , ...
  'XMinorTick'  , 'off'      , ...
  'YMinorTick'  , 'off'      , ...
  'YGrid'       , 'on'      , ...
  'XColor'      , [.3 .3 .3], ...
  'YColor'      , [.3 .3 .3], ...
  'LineWidth'   , 1.2         );
